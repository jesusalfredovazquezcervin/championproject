package com.koomoni.bean;

import java.io.Serializable;

public class Almacen implements Serializable {

	private static final long serialVersionUID = 1L;
	private Integer claveAlmacen;
	private String descripcion;
	private String direccion;
	private String encargado;
	private String telefono;
	private String cuentaContable;
	private Integer cveMent;
	private Integer cveMsal;
	private String status;

	public Integer getClaveAlmacen() {
		return claveAlmacen;
	}

	public void setClaveAlmacen(Integer claveAlmacen) {
		this.claveAlmacen = claveAlmacen;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	public String getDireccion() {
		return direccion;
	}

	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}

	public String getEncargado() {
		return encargado;
	}

	public void setEncargado(String encargado) {
		this.encargado = encargado;
	}

	public String getTelefono() {
		return telefono;
	}

	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}

	public String getCuentaContable() {
		return cuentaContable;
	}

	public void setCuentaContable(String cuentaContable) {
		this.cuentaContable = cuentaContable;
	}

	public Integer getCveMent() {
		return cveMent;
	}

	public void setCveMent(Integer cveMent) {
		this.cveMent = cveMent;
	}

	public Integer getCveMsal() {
		return cveMsal;
	}

	public void setCveMsal(Integer cveMsal) {
		this.cveMsal = cveMsal;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}
}
